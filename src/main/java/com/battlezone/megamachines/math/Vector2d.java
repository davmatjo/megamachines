package com.battlezone.megamachines.math;

/**
 * A 2D vector class for doubles.
 */
public class Vector2d {

    public double x;
    public double y;

    /**
     * Creates a 2D vector with the set values.
     *
     * @param x the X value.
     * @param y the Y value.
     */
    public Vector2d(double x, double y) {
        this.x = x;
        this.y = y;
    }
}